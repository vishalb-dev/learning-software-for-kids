//Function to design border//
void border(int xmax,int ymax,int n)
{
    int x=0,y=0,i;
    for(i=0;i<=n;i++)
    {
	rectangle(x,y,xmax,ymax);
	x++;
	y++;
	xmax--;
	ymax--;
    }
}
